# While Loop
# It is a type of control statement which will run a statement or a set 
# of statements repeatedly unless the given condition becomes 
# false.
# Syntax:
# while ( condition )
#  { 
#      statement 
# }


# R program to demonstrate the use of while loop
   
val = 1
   
# using while loop
while (val <= 5 ) 
{
    # statements
    print(val)
    val = val + 1
}